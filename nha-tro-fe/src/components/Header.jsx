import React, { useState, useEffect } from "react";
import { Link, useNavigate } from "react-router-dom";
import { motion, AnimatePresence } from "framer-motion";
import {
  Navbar,
  Nav,
  Form,
  FormControl,
  Button,
  Container,
} from "react-bootstrap";
import { FaAngleDown, FaBell } from "react-icons/fa";
import { useUser } from "../contexts/UserContext";
import Modal from "./Modal.jsx";
import { ToastContainer, toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";

const TENANT_API = "http://localhost:8000/tenants/from-user/";
const NOTIFICATION_API = "http://localhost:8000/notifications/user/";

export default function Header() {
  const navigate = useNavigate();
  const { currentUser, logout } = useUser();

  const [dropdownOpen, setDropdownOpen] = useState(false);
  const [showProfile, setShowProfile] = useState(false);
  const [showChangePassword, setShowChangePassword] = useState(false);
  const [passwordForm, setPasswordForm] = useState({
    oldPassword: "",
    newPassword: "",
    confirmPassword: ""
  });
  const [loading, setLoading] = useState(false);
  const [tenantInfo, setTenantInfo] = useState(null);
  const [notifications, setNotifications] = useState([]);
  const [showNotiDropdown, setShowNotiDropdown] = useState(false);
  const [unreadCount, setUnreadCount] = useState(0);

  // L·∫•y token t·ª´ localStorage m·ªói l·∫ßn ƒë·ªïi m·∫≠t kh·∫©u
  const getToken = () => localStorage.getItem("token");

  useEffect(() => {
    const handleClick = (e) => {
      if (!e.target.closest('.dropdown-user-header')) setDropdownOpen(false);
    };
    if (dropdownOpen) document.addEventListener('mousedown', handleClick);
    return () => document.removeEventListener('mousedown', handleClick);
  }, [dropdownOpen]);

  // Khi m·ªü modal profile, l·∫•y th√¥ng tin tenant
  useEffect(() => {
    if (showProfile && currentUser?.id) {
      fetch(`${TENANT_API}${currentUser.id}`)
        .then(res => res.json())
        .then(data => setTenantInfo(data))
        .catch(() => setTenantInfo(null));
    }
  }, [showProfile, currentUser]);

  // L·∫•y th√¥ng b√°o khi m·ªü dropdown
  useEffect(() => {
    if (showNotiDropdown && currentUser?.id) {
      fetch(`${NOTIFICATION_API}${currentUser.id}?skip=0&limit=10`)
        .then(res => res.json())
        .then(data => setNotifications(Array.isArray(data) ? data : []))
        .catch(() => setNotifications([]));
    }
  }, [showNotiDropdown, currentUser]);

  // L·∫•y s·ªë l∆∞·ª£ng th√¥ng b√°o ch∆∞a ƒë·ªçc m·ªói khi user ƒëƒÉng nh·∫≠p ho·∫∑c khi dropdown m·ªü
  useEffect(() => {
    if (currentUser?.id) {
      fetch(`http://localhost:8000/notifications/user/${currentUser.id}/unread-count`)
        .then(res => res.json())
        .then(count => setUnreadCount(count))
        .catch(() => setUnreadCount(0));
    }
  }, [currentUser, showNotiDropdown, notifications]);

  // Khi ƒë√°nh d·∫•u ƒë√£ ƒë·ªçc, c·∫≠p nh·∫≠t l·∫°i s·ªë l∆∞·ª£ng ch∆∞a ƒë·ªçc
  const handleReadNotification = async (noti) => {
    if (!noti.is_read) {
      await fetch(`http://localhost:8000/notifications/read/${noti.notification_id}`, { method: "PUT" });
      setNotifications(nots => nots.map(n => n.notification_id === noti.notification_id ? { ...n, is_read: true } : n));
      // G·ªçi l·∫°i API ƒë·ªÉ c·∫≠p nh·∫≠t s·ªë l∆∞·ª£ng ch∆∞a ƒë·ªçc
      if (currentUser?.id) {
        fetch(`http://localhost:8000/notifications/user/${currentUser.id}/unread-count`)
          .then(res => res.json())
          .then(count => setUnreadCount(count))
          .catch(() => setUnreadCount(0));
      }
    }
    // C√≥ th·ªÉ chuy·ªÉn h∆∞·ªõng ho·∫∑c hi·ªÉn th·ªã chi ti·∫øt t·∫°i ƒë√¢y n·∫øu mu·ªën
  };

  const handleNavigate = (path) => {
    navigate(path);
  };

  const handleLogout = () => {
    logout();
    navigate("/login");
  };

  const handleOpenProfile = () => {
    setShowProfile(true);
  };

  const handleOpenChangePassword = () => {
    setShowChangePassword(true);
    setShowProfile(false);
  };

  // T√≠ch h·ª£p API ƒë·ªïi m·∫≠t kh·∫©u
  const handleChangePassword = async (e) => {
    e.preventDefault();
    const token = getToken();
    if (!passwordForm.oldPassword || !passwordForm.newPassword || !passwordForm.confirmPassword) {
      toast.error("Vui l√≤ng nh·∫≠p ƒë·∫ßy ƒë·ªß th√¥ng tin!");
      return;
    }
    if (passwordForm.newPassword !== passwordForm.confirmPassword) {
      toast.error("M·∫≠t kh·∫©u m·ªõi kh√¥ng kh·ªõp!");
      return;
    }
    setLoading(true);
    try {
      const res = await fetch("http://localhost:8000/auth/change-password", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          Authorization: token ? `Bearer ${token}` : "",
        },
        body: JSON.stringify({
          old_password: passwordForm.oldPassword,
          new_password: passwordForm.newPassword,
        }),
      });
      if (!res.ok) {
        const data = await res.json();
        throw new Error(data.detail || "ƒê·ªïi m·∫≠t kh·∫©u th·∫•t b·∫°i!");
      }
      setShowChangePassword(false);
      setPasswordForm({ oldPassword: "", newPassword: "", confirmPassword: "" });
      toast.success("‚úÖ ƒê·ªïi m·∫≠t kh·∫©u th√†nh c√¥ng!");
    } catch (err) {
      toast.error(err.message || "ƒê·ªïi m·∫≠t kh·∫©u th·∫•t b·∫°i!");
    } finally {
      setLoading(false);
    }
  };

  return (
    <motion.div
      initial={{ y: -70, opacity: 0 }}
      animate={{ y: 0, opacity: 1 }}
      transition={{ duration: 0.6 }}
    >
      <Navbar
        expand="lg"
        style={{ backgroundColor: "#004643" }}
        variant="dark"
        className="shadow"
      >
        <Container>
          <Navbar.Brand as={Link} to="/" className="fw-bold" style={{ color: "#f9bc60" }}>
            üè† Nh√† Tr·ªç B·∫£o B·∫£o
          </Navbar.Brand>
          <Navbar.Toggle aria-controls="navbar-nav" />
          <Navbar.Collapse id="navbar-nav">
            <Nav className="me-auto">
              <Nav.Link as={Link} to="/rooms" style={{ color: "#ffffff" }}>Ph√≤ng</Nav.Link>
              <Nav.Link as={Link} to="/contracts" style={{ color: "#ffffff" }}>H·ª£p ƒë·ªìng</Nav.Link>
              <Nav.Link as={Link} to="/payments" style={{ color: "#ffffff" }}>Thanh to√°n</Nav.Link>
              <Nav.Link as={Link} to="/support" style={{ color: "#ffffff" }}>H·ªó tr·ª£</Nav.Link>
            </Nav>

            {/* Badge th√¥ng b√°o n·∫±m b√™n tr√°i AnimatePresence */}
            <div className="dropdown d-inline-block position-relative me-3">
              <button
                type="button"
                className="btn btn-warning position-relative"
                style={{ fontWeight: "bold" }}
                onClick={() => setShowNotiDropdown(open => !open)}
              >
                <FaBell className="me-1" />
                {unreadCount > 0 && (
                  <span className="position-absolute top-0 start-100 translate-middle badge rounded-pill bg-danger">
                    {unreadCount}
                    <span className="visually-hidden">unread messages</span>
                  </span>
                )}
              </button>
              <ul
                className={`dropdown-menu${showNotiDropdown ? " show" : ""}`}
                style={{ position: "absolute", top: "100%", right: 0, zIndex: 1000, minWidth: "320px", maxHeight: "400px", overflowY: "auto" }}
              >
                {notifications.length === 0 ? (
                  <li className="dropdown-item text-muted">Kh√¥ng c√≥ th√¥ng b√°o n√†o.</li>
                ) : (
                  notifications.map(noti => (
                    <li
                      key={noti.notification_id}
                      className={`dropdown-item${noti.is_read ? "" : " fw-bold text-dark"}`}
                      style={{ cursor: "pointer", background: noti.is_read ? "#fff" : "#ffeeba" }}
                      onClick={() => handleReadNotification(noti)}
                    >
                      <div>{noti.title}</div>
                      <div className="small text-secondary">{noti.message}</div>
                    </li>
                  ))
                )}
              </ul>
            </div>

            <AnimatePresence mode="wait">
              {!currentUser ? (
                <motion.div
                  key="auth-menu"
                  whileHover={{ scale: 1.05 }}
                  whileTap={{ scale: 0.95 }}
                  initial={{ opacity: 0 }}
                  animate={{ opacity: 1 }}
                  exit={{ opacity: 0 }}
                >
                  <Button
                    variant="outline-light"
                    className="me-2"
                    onClick={() => handleNavigate("/login")}
                  >
                    ƒêƒÉng nh·∫≠p
                  </Button>
                  <Button
                    variant="warning"
                    onClick={() => handleNavigate("/register")}
                  >
                    ƒêƒÉng k√Ω
                  </Button>
                </motion.div>
              ) : (
                <motion.div
                  key="user-avatar"
                  whileHover={{ scale: 1.1 }}
                  whileTap={{ scale: 0.95 }}
                  initial={{ opacity: 0 }}
                  animate={{ opacity: 1 }}
                  exit={{ opacity: 0 }}
                >
                  <div className="dropdown dropdown-user-header d-inline-block position-relative">
                    <Button
                      variant="link"
                      className="text-white d-flex align-items-center"
                      style={{ textDecoration: "none" }}
                      onClick={() => setDropdownOpen((open) => !open)}
                    >
                      <span style={{ color: "#ffffff" }}>
                        üë§ {currentUser?.username || "User"}
                      </span>
                      <FaAngleDown className="ms-1" />
                    </Button>
                    <ul
                      className={`dropdown-menu dropdown-menu-end${dropdownOpen ? " show" : ""}`}
                      style={{ position: "absolute", top: "100%", right: 0, zIndex: 1000 }}
                    >
                      <li>
                        <button className="dropdown-item" onClick={() => { setShowProfile(true); setDropdownOpen(false); }}>
                          Th√¥ng tin c√° nh√¢n
                        </button>
                      </li>
                      <li>
                        <button className="dropdown-item" onClick={() => { setShowChangePassword(true); setDropdownOpen(false); }}>
                          ƒê·ªïi m·∫≠t kh·∫©u
                        </button>
                      </li>
                      <li><hr className="dropdown-divider" /></li>
                      <li>
                        <button className="dropdown-item text-danger" onClick={handleLogout}>
                          ƒêƒÉng xu·∫•t
                        </button>
                      </li>
                    </ul>
                  </div>
                </motion.div>
              )}
            </AnimatePresence>
          </Navbar.Collapse>
        </Container>
      </Navbar>

      {/* Modal Th√¥ng tin c√° nh√¢n */}
      <Modal
        isOpen={showProfile}
        onClose={() => setShowProfile(false)}
        title="üë§ Th√¥ng tin c√° nh√¢n"
        showConfirm={false}
      >
        {tenantInfo ? (
          <div>
            <div className="mb-2"><b>H·ªç t√™n:</b> {tenantInfo.full_name}</div>
            <div className="mb-2"><b>Email:</b> {tenantInfo.email}</div>
            <div className="mb-2"><b>SƒêT:</b> {tenantInfo.phone_number}</div>
            <div className="mb-2"><b>Ng√†y sinh:</b> {tenantInfo.date_of_birth}</div>
            <div className="mb-2"><b>Gi·ªõi t√≠nh:</b> {tenantInfo.gender}</div>
            <div className="mb-2"><b>ƒêang thu√™:</b> {tenantInfo.is_rent ? "C√≥" : "Kh√¥ng"}</div>
            <button className="btn btn-link p-0" onClick={() => { setShowProfile(false); setShowChangePassword(true); }}>
              ƒê·ªïi m·∫≠t kh·∫©u
            </button>
          </div>
        ) : (
          <div>Kh√¥ng c√≥ th√¥ng tin ng∆∞·ªùi d√πng.</div>
        )}
      </Modal>

      {/* Modal ƒê·ªïi m·∫≠t kh·∫©u */}
      <Modal
        isOpen={showChangePassword}
        onClose={() => setShowChangePassword(false)}
        title="üîí ƒê·ªïi m·∫≠t kh·∫©u"
        showConfirm={false}
      >
        <form onSubmit={handleChangePassword}>
          <div className="mb-3">
            <label className="form-label">M·∫≠t kh·∫©u c≈©</label>
            <input
              type="password"
              className="form-control"
              value={passwordForm.oldPassword}
              onChange={e => setPasswordForm(f => ({ ...f, oldPassword: e.target.value }))}
              required
            />
          </div>
          <div className="mb-3">
            <label className="form-label">M·∫≠t kh·∫©u m·ªõi</label>
            <input
              type="password"
              className="form-control"
              value={passwordForm.newPassword}
              onChange={e => setPasswordForm(f => ({ ...f, newPassword: e.target.value }))}
              required
            />
          </div>
          <div className="mb-3">
            <label className="form-label">Nh·∫≠p l·∫°i m·∫≠t kh·∫©u m·ªõi</label>
            <input
              type="password"
              className="form-control"
              value={passwordForm.confirmPassword}
              onChange={e => setPasswordForm(f => ({ ...f, confirmPassword: e.target.value }))}
              required
            />
          </div>
          <button type="submit" className="btn btn-primary" disabled={loading}>
            {loading ? "ƒêang ƒë·ªïi..." : "ƒê·ªïi m·∫≠t kh·∫©u"}
          </button>
        </form>
      </Modal>
      <ToastContainer position="top-right" autoClose={3000} hideProgressBar={false} newestOnTop={false} closeOnClick={true} rtl={false} pauseOnFocusLoss={true} draggable={true} pauseOnHover={true} />
    </motion.div>
  );
}
